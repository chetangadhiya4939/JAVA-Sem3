import java.util.*;
public class mathOperations {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter the 1st Number a : ");
        int a=sc.nextInt();
        System.out.print("Enter the 2nd Number b : ");
        int b=sc.nextInt();

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

    }
}
